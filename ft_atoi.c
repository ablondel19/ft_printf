#include "ft_printf.h"

int		ft_atoi(char *s)
{
	int res;
	int neg;

	res = 0;
	neg = 1;
	if (*s == '-')
	{
		neg = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
		res = res * 10 + *s++ - '0';
	return (res * neg);
}
